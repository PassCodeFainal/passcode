package pass.vo;

public class IntroDto {
	private String intro_no, intro_id, intro_title, intro_que, intro_ans;
	
	public String getIntro_no() {
		return intro_no;
	}
	public void setIntro_no(String intro_no) {
		this.intro_no = intro_no;
	}
	public String getIntro_id() {
		return intro_id;
	}
	public void setIntro_id(String intro_id) {
		this.intro_id = intro_id;
	}
	public String getIntro_title() {
		return intro_title;
	}
	public void setIntro_title(String intro_title) {
		this.intro_title = intro_title;
	}
	public String getIntro_que() {
		return intro_que;
	}
	public void setIntro_que(String intro_que) {
		this.intro_que = intro_que;
	}
	public String getIntro_ans() {
		return intro_ans;
	}
	public void setIntro_ans(String intro_ans) {
		this.intro_ans = intro_ans;
	}
	
	
	
}
