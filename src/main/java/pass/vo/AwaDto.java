package pass.vo;

public class AwaDto {
	private String awa_no,awa_id, awa_title, awa_content, awa_institute, awa_date;
	
	public String getAwa_no() {
		return awa_no;
	}
	public void setAwa_no(String awa_no) {
		this.awa_no = awa_no;
	}
	public String getAwa_id() {
		return awa_id;
	}
	public void setAwa_id(String awa_id) {
		this.awa_id = awa_id;
	}
	public String getAwa_title() {
		return awa_title;
	}
	public void setAwa_title(String awa_title) {
		this.awa_title = awa_title;
	}
	public String getAwa_content() {
		return awa_content;
	}
	public void setAwa_content(String awa_content) {
		this.awa_content = awa_content;
	}
	public String getAwa_institute() {
		return awa_institute;
	}
	public void setAwa_institute(String awa_institute) {
		this.awa_institute = awa_institute;
	}
	public String getAwa_date() {
		return awa_date;
	}
	public void setAwa_date(String awa_date) {
		this.awa_date = awa_date;
	}
	
}
